import java.lang.Integer;
public class signnumber 
{
public static void main(String args[])
{
int A=10,B=0,C=-12;
System.out.println(Integer.signum(A));
System.out.println(Integer.signum(B));
System.out.println(Integer.signum(C));
/*
?Returns the signum function of the 
?specified int value. (The return value 
?is -1 if the specified value is negative;
?0 if the specified value is zero; and 1 
? if the specified value is positive.)
*/
}   
}